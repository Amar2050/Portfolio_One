function dynamicNav() {
    var x = document.getElementById("NavBarHamburger");
    if (x.style.display === "block") 
    {
        x.style.display = "none";
    } 
    else 
    {
        x.style.display = "block";
    }
}
function closingNav() {
    var x = document.getElementById("NavBarHamburger");
    if (x.style.display === "block") 
    {
        x.style.display = "none";
    }
}
